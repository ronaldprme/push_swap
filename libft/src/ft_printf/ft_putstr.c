#include "ft_printf.h"

void	ft_putstr(char *s, int *count)
{
	int	i;

	i = 0;
	if (s)
	{
		while (s[i] != '\0')
		{
			ft_putchar(s[i], count);
			i++;
		}
	}
	else if (!s)
	{
		ft_putstr("(null)", count);
	}
}
