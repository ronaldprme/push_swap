#include "libft.h"

char	*ft_strdup(const char *str)
{
	char	*ptr;
	size_t	n;
	size_t	i;

	n = 0;
	while (str[n] != '\0')
		n++;
	ptr = malloc((n + 1) * sizeof(char));
	if (!ptr)
		return (NULL);
	i = 0;
	while (str[i] != '\0')
	{
		ptr[i] = str[i];
		i++;
	}
	ptr[i] = '\0';
	return (ptr);
}
